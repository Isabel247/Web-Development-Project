                    <p>The church is not a building, but a body of believers with a specific nature and purpose. These biblical roles or ministries of the church are foundational to it. What are these roles? They are many, but key to any church are foundations in worship, edification and evangelism. Worship is God-centered and Christ-centered. It is not about entertaining Christians with flashy displays or presentations, but about expressing our love by worshiping our Creator. We are to praise and glorify God in worship. As such, every Christian needs to be part of regular fellowship and worship. Edification is also a role of the church. It involves edifying believers, but also nurturing, building up or helping believers to mature in Christ. To this end, churches are tasked with a variety of ministries such as Bible study, continuing education in related areas, praying for one another, acts of genuine hospitality and more. Evangelism is also a key role of the church. This means reaching out to a lost world with the Good News about Jesus. </p>
